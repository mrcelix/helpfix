// Bu dosya elle yazıldı; ileride `supabase gen types typescript` ile
// otomatik üretilenle değiştirilebilir. Şimdilik Faz D (Servis Masası)
// için gereken tabloları ve çekirdek (tenant/kullanıcı) tablolarını
// kapsıyor. Yeni modül eklendikçe buraya yeni tablo tipleri eklenecek.

export type Priority = 'P1' | 'P2' | 'P3' | 'P4'
export type TicketStatus =
  | 'new'
  | 'open'
  | 'in_progress'
  | 'on_hold'
  | 'resolved'
  | 'closed'
  | 'merged'
export type TicketChannel = 'portal' | 'email' | 'chat' | 'phone' | 'teams'
export type UserRole = 'tenant_admin' | 'manager' | 'agent' | 'requester'

export interface Database {
  public: {
    Tables: {
      tenants: {
        Row: {
          id: string
          name: string
          slug: string
          plan: 'starter' | 'growth' | 'enterprise'
          created_at: string
        }
        Insert: Omit<Database['public']['Tables']['tenants']['Row'], 'id' | 'created_at'> & {
          id?: string
          created_at?: string
        }
        Update: Partial<Database['public']['Tables']['tenants']['Insert']>
        Relationships: []
      }
      user_profiles: {
        Row: {
          id: string
          tenant_id: string
          auth_user_id: string
          full_name: string
          email: string
          role: UserRole
          department_id: string | null
          avatar_initials: string | null
          is_active: boolean
          created_at: string
        }
        Insert: Omit<Database['public']['Tables']['user_profiles']['Row'], 'id' | 'created_at'> & {
          id?: string
          created_at?: string
        }
        Update: Partial<Database['public']['Tables']['user_profiles']['Insert']>
        Relationships: []
      }
      departments: {
        Row: {
          id: string
          tenant_id: string
          name: string
          manager_id: string | null
        }
        Insert: Omit<Database['public']['Tables']['departments']['Row'], 'id'> & { id?: string }
        Update: Partial<Database['public']['Tables']['departments']['Insert']>
        Relationships: []
      }
      incidents: {
        Row: {
          id: string
          tenant_id: string
          ref: string // e.g. INC-000418, generated by trigger
          title: string
          description: string | null
          priority: Priority
          status: TicketStatus
          channel: TicketChannel
          category: string | null
          requester_id: string
          assignee_id: string | null
          possible_duplicate_of: string | null
          sla_policy_id: string | null
          sla_due_at: string | null
          csat_score: number | null
          created_at: string
          updated_at: string
          resolved_at: string | null
          closed_at: string | null
        }
        Insert: Omit<
          Database['public']['Tables']['incidents']['Row'],
          'id' | 'ref' | 'created_at' | 'updated_at'
        > & {
          id?: string
          ref?: string
          created_at?: string
          updated_at?: string
        }
        Update: Partial<Database['public']['Tables']['incidents']['Insert']>
        Relationships: []
      }
      incident_comments: {
        Row: {
          id: string
          incident_id: string
          author_id: string
          body: string
          is_internal: boolean
          created_at: string
        }
        Insert: Omit<Database['public']['Tables']['incident_comments']['Row'], 'id' | 'created_at'> & {
          id?: string
          created_at?: string
        }
        Update: Partial<Database['public']['Tables']['incident_comments']['Insert']>
        Relationships: []
      }
      incident_timeline: {
        Row: {
          id: string
          incident_id: string
          actor_id: string | null
          event_type: string // 'created' | 'assigned' | 'status_changed' | 'ai_action' | ...
          event_data: Record<string, unknown> | null
          created_at: string
        }
        Insert: Omit<Database['public']['Tables']['incident_timeline']['Row'], 'id' | 'created_at'> & {
          id?: string
          created_at?: string
        }
        Update: Partial<Database['public']['Tables']['incident_timeline']['Insert']>
        Relationships: []
      }
    }
    Views: Record<string, never>
    Functions: Record<string, never>
    Enums: Record<string, never>
  }
}
